package Assignment6;

public class AddNumber {

    public AddNumber(int num1,int num2)
    {
        System.out.println(num1+num2);
    }

    public AddNumber(int num1,int num2,int num3)
    {
        System.out.println(num1+num2+num3);
    }

    public AddNumber(double num1,double num2)
    {
        System.out.println(num1+num2);
    }

    public AddNumber(double num1,double num2,double num3)
    {
        System.out.println(num1+num2+num3);
    }
}
